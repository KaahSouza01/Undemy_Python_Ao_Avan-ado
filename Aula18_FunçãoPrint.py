"""
Esses números ou coisas aleatorias colocadas dentro
são chamados de argumentos não nomeados
"""
print(123)
print(12, 13)

"""
Podemos usar o SEP, um separador que podemos escolher
qual separador queremos ver exibido na tela
o Sep é um argumento nomeado
"""
print(56, 78, sep=',')
print(56, 78, sep='/')
print(56, 78, sep='-')
print(56, 78, sep='$')